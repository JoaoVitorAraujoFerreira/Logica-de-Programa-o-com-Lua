
--caso vc quaira colocar aspas "" dentro de uma string vc deve usar a \ (caracter de escape)
print("copo d\'agua")
print("a melhor \"idioma\" do munto inteiro")
-- para vc escrver a barra em um testo vc deve colocar uma \ extra
print("barra \\")

-- caso vc quaira escrever algo em varias linhas vc deve colocar \n

print("Posso pressentir o perigo e o caos\nE ninguém agora vai me amedrontar\nCom a minha mente vou a mil lugares\nE a imaginação me dá forças pra voar")

--caso vc queira colocar uma tabulacao no texto coloque \t e para voltar \r

print("\tTabulação")


